class ElseIfExample {

   public static void main(String[] args){
      int my_fav_num = 1;
   
      if(my_fav_num == 1){
         System.out.println("One is the loneliest number.");
      }
      
      else if(my_fav_num == 2){
         System.out.println("Two can be as bad as one.");
      }
      
      else{
         System.out.println("Your favorite number isn't 1 or 2.");
      }
      
   }
}
